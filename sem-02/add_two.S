// Задача: считать со строки ввода два числа и вывести их сумму
.intel_syntax noprefix
    .global main
main:
    // считать из строки ввода число в eax
    call    readi32
    // переложим считанное число в edi, чтобы можно было еще раз вызвать readi32
    // и потерять считанное ранее число
    mov     edi, eax 

    // считываем второе число
    call    readi32

    // так как одно число уже лежит в edi, а второе мы считали только что в eax
    // то можем сложить eax и edi. Складываем так, чтобы вторым операндом был edi
    // чтобфы избежать лишнего перекладывания в edi для вывода через writei32
    add     edi, eax

    // выводим число из регистра edi
    call    writei32
    // завершаем выполнение
    call    finish
